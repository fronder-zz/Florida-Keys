#import "UIFont+FKFont.h"

@implementation UIFont (FKFont)

+ (UIFont *)robotoBlackSized:(CGFloat)size {
    return [UIFont fontWithName:@"Roboto-Black" size:size];
}

+ (UIFont *)robotoBoldSized:(CGFloat)size {
    return [UIFont fontWithName:@"Roboto-Bold" size:size];
}

@end
